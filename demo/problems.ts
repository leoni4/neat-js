export const testSin01 = {
    input: [
        [-3.1416], // -pi
        [-2.3562], // -3pi/4
        [-1.5708], // -pi/2
        [-0.7854], // -pi/4
        [0.0],
        [0.7854],
        [1.5708],
        [2.3562],
        [3.1416], // +pi
    ],
    output: [
        [0.5], // sin(-pi)=0 -> 0.5
        [0.1464], // sin(-3pi/4)=-0.707 -> ~0.146
        [0.0], // sin(-pi/2)=-1 -> 0
        [0.1464], // -0.707 -> ~0.146
        [0.5], // 0 -> 0.5
        [0.8536], // +0.707 -> ~0.854
        [1.0], // +1 -> 1
        [0.8536], // +0.707 -> ~0.854
        [0.5], // 0 -> 0.5
    ],
};
